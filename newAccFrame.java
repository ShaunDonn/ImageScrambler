    private void setraf(String key, String idnum, String firstName, String lastName, String password){//sets the raf only for rowers
        String fullrecord = "";
        try {
            int place = (int) (rowers.length()/101);//finds the number of records in the raf
            fullrecord += key+padtext(idnum,7)+padtext(firstName,15)+padtext(lastName,20)+padtext(password,20); //makes record a String
            rowers.seek(place*101);//goes to next open record in raf
            rowers.writeBytes(fullrecord);//sets record there
            rowers.seek(place*101+65);//goes to the end of that record
            rowers.writeBytes(padtext(Rcoachname.getText(),35)+"\n");//adds the rower's coach's name
        } catch (IOException ex) {
            Logger.getLogger(newAccFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private String padtext(String string, int length){//pads the text with x's so it fits right in the record
        int add = length - string.length();//finds how many x's need to be added
        for (int i = 0; i < add; i++) {//
            string += 'x';//adds the right number of x's to the end of the String
        }
        return string;//returns the padded String to be put in the raf
    }
    private String padspot(int spot){//pads the key number so it's 3 digits
        String paddedspot="";
        for (int i = 0; i < 3-String.valueOf(spot).length(); i++) {
            paddedspot+=0;//adds 0's in front of the int to make it a 3 digit number
        }
        paddedspot+=String.valueOf(spot);//paddedspot becomes a String of the 3-digit int
        return paddedspot;//returns the padded number
    }
