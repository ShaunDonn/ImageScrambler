    private String padkey(String key){
        String paddedkey = key;
        for (int i = 0; i < 39; i++) {
            paddedkey += 'x';
        }
        System.out.println(paddedkey);
        return paddedkey;
    }
    private String padVal(String key){
        String doneKey = "";
        for (int i = 0; i < 2-key.length(); i++) {
            doneKey += "0";
        }
        doneKey += key;
        return doneKey;
    }
    private String fixpassword (char [] letters){//brings in the password as an array of chars (cause that's the format) and makes is a String
        String password = "";
        for (int i = 0; i < letters.length; i++) {
            password += letters [i];
        }
        return password;
    }
